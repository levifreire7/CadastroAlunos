    private void jTableAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAlunosMouseClicked
        // TODO add your handling code here:
        jTFMatricula.setText(jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 0).toString());
        jTFNome.setText(jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 1).toString());
        String sexo = jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 2).toString();
        
        if(sexo.equals("Masculino")){
            jRBMasculino.setSelected(true);
        }else{
            jRBFeminino.setSelected(true);
        }
        
        jTFTelefone.setText(jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 3).toString());
        jTFEndereco.setText(jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 4).toString());               
    }//GEN-LAST:event_jTableAlunosMouseClicked

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        int matricula = Integer.parseInt(jTFMatricula.getText());
        String nome = jTFNome.getText();
        String sexo;
        String telefone = jTFTelefone.getText();
        String endereco = jTFEndereco.getText();
        
        if(jRBMasculino.isSelected()){
            sexo = "Masculino";
        }else{
            sexo = "Feminino";
        }
              
        for(Aluno a : alunos){
            if(a.getMatricula() == matricula){
                a.setMatricula(matricula);
                a.setNome(nome);
                a.setSexo(sexo);
                a.setTelefone(telefone);
                a.setEndereco(endereco);
            }
        }
        
        atualizaTabela();
        limpaCampos();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int matricula = Integer.parseInt(jTFMatricula.getText());
          
        for(int i = 0; i < alunos.size(); i++){
            if(alunos.get(i).getMatricula() == matricula){
                alunos.remove(alunos.get(i));
            }
        }
        
        atualizaTabela();
        limpaCampos();
    }//GEN-LAST:event_btnExcluirActionPerformed

