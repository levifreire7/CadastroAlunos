    private void atualizaTabela(){
        DefaultTableModel model =(DefaultTableModel) jTableAlunos.getModel();
        model.setNumRows(0);
        
        for(Aluno a : alunos){
            model.addRow(new Object[]{
                a.getMatricula(),
                a.getNome(),
                a.getSexo(),
                a.getTelefone(),
                a.getEndereco()
            });
        }
    }
    
    private void limpaCampos(){
        jTFMatricula.setText("");
        jTFNome.setText("");
        jTFTelefone.setText("");
        jTFEndereco.setText("");
        jRBFeminino.setSelected(false);
        jRBMasculino.setSelected(false);
    }
    
